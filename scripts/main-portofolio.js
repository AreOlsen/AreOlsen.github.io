/*
A generator for generating the portfolio tiles.
On the main page, the portfolio tiles are generated by this script.
*/

// The portfolio tile generator.

function generatatePortfolioTiles() {
	fetch("scripts/portfolio-data.json")
		.then((Response) => Response.json())
		.then((data) => {
			let html = "";
			for (let i = 0; i < data.covers.length; i++) {
				html += `<a class="portfolio-tile" href="${data.links[i]}" target="_blank" rel="noopener"  style='background-image:url("/scripts/portfoliodata/covers/${data.covers[i]}") !important;'>
                        <div class="portfolio-tile-description">
                            <h3>${data.names[i]}</h3>
				         <p>${data.descriptions[i]}</p>
                        </div>
            	    </a>`;
			}
			document.getElementById("tiles").innerHTML = html;
		});
}

generatatePortfolioTiles();
